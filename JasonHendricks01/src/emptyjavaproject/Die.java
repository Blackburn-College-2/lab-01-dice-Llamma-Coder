package emptyjavaproject;

/**
 *
 * @author jason.hendricks
 */
public class Die {

    int sides;

    int dice;

    int numPrints;

    public int getNumPrints() {
        return numPrints;
    }

    public void setNumPrints(int numPrints) {
        this.numPrints = numPrints;
    }

    public int getSides() {
        return sides;
    }

    public int getDice() {
        return dice;
    }

    public void setSides(int sides) {
        this.sides = sides;
    }

    public void setDice(int dice) {
        this.dice = dice;
    }

}
